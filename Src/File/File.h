#pragma once


#include <fstream>
#include <string>

#define MAX_PATH_SIZE 1024

namespace Framework
{
	namespace File
	{
		//--------------------------------------------------------------------//
		/// @class File
		/// @brief File manager
		//--------------------------------------------------------------------//
		class File {
		public:

			//--------------------------------------------------------------------//
			/// @brief Default constructor : do nothing
			//--------------------------------------------------------------------//
			File(){}

			//--------------------------------------------------------------------//
			/// @brief                              Construct a File from a fileName
			/// @param filename     The name of the file
			//--------------------------------------------------------------------//
			File(const char* filename);

			//--------------------------------------------------------------------//
			/// @brief Default destructor : do nothing.
			/// Must be virtual
			//--------------------------------------------------------------------//
			~File(){}

			//--------------------------------------------------------------------//
			/// @brief              Set the name of the file
			/// @param file The name of the file
			//--------------------------------------------------------------------//
			void setFileName(std::string file);

			//--------------------------------------------------------------------//
			/// @brief Get the fileName extension
			//--------------------------------------------------------------------//
			std::string GetExtension();

			//--------------------------------------------------------------------//
			/// @brief Get the short name of the file
			/// Note : Not Implemented
			//--------------------------------------------------------------------//
			std::string GetShortName();

			//--------------------------------------------------------------------//
			/// @brief Get the fileName
			//--------------------------------------------------------------------//
			std::string GetFileName();

			//--------------------------------------------------------------------//
			/// @brief Get the fullPathFileName
			//--------------------------------------------------------------------//
			const std::string& GetFullPathName();

		protected:
			//--------------------------------------------------------------------//
			/// @brief std::string to store the fileName
			//--------------------------------------------------------------------//
			std::string fileName;
		};
	}
}
